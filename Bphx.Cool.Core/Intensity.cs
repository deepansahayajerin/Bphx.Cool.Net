namespace Bphx.Cool;

/// <summary>
/// Defines a screen field insensity.
/// </summary>
public enum Intensity
{
  /// <summary>
  /// A field is displayed at normal intensity.
  /// </summary>
  Normal,

  /// <summary>
  /// A field is displayed at brighter than normal intensity 
  /// (a highlightened field).
  /// </summary>
  High,

  /// <summary>
  /// A field is not displayed.
  /// </summary>
  Dark
}
