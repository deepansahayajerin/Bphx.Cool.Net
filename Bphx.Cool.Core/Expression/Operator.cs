namespace Bphx.Cool.Expression;

/// <summary>
/// Defines an expression operator.
/// </summary>
public enum Operator
{
  /// <summary>
  /// Equal operator.
  /// </summary>
  Equals,
  /// <summary>
  /// Not equal operator.
  /// </summary>
  NotEquals,
  /// <summary>
  /// Less than operator.
  /// </summary>
  LessThan,
  /// <summary>
  /// Greater than operator.
  /// </summary>
  GreaterThan,
  /// <summary>
  /// Less than or equal operator.
  /// </summary>
  LessThanOrEquals,
  /// <summary>
  /// Greater than or equal operator.
  /// </summary>
  GreaterThanOrEqauls,
  /// <summary>
  /// Logical not operator.
  /// </summary>
  Not,
  /// <summary>
  /// Logical and operator.
  /// </summary>
  And,
  /// <summary>
  /// Logical or operator.
  /// </summary>
  Or
}
