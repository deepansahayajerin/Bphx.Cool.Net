namespace Bphx.Cool.UI;

/// <summary>
/// Initial window position.
/// </summary>
public enum InitialPosition
{
  /// <summary>
  /// Initial position is explicitly specified.
  /// </summary>
  Designed,

  /// <summary>
  /// Initial position is selected automatically.
  /// </summary>
  System,

  /// <summary>
  /// Initial position is selected relative to mouse position.
  /// </summary>
  Mouse
}
